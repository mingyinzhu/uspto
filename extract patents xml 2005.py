def filter(title, abstract, summary, description, claims):
    words = []
    finance_temp = finance_terms
    for word in finance_temp:
        if word in title or word in abstract or word in description or word in claims:
            words.append(word)
            finance_temp.remove(word)
            for word2 in finance_temp:
                if word2 in title or word2 in abstract or word2 in description or word2 in claims:
                    words.append(word)
                    return True, words
    return False, words


def strip_for_csv(string):
    if string is None:
        return None
    else:
        return string.replace('"', '').replace("\n", "").replace("\r", "").replace(" |", ", ").replace("| ",
                                                                                                       ", ").replace(
            "|", ",").replace("\\", "").strip()


def return_element_text(xmlElement):
    if (ET.iselement(xmlElement)):
        elementStr = ET.tostring(xmlElement).decode('utf-8')
        # Strip tags, whitespace and newline and carriage returns
        element_text = re.sub('<[^<]*>', ' ', elementStr)
        # Strip leading and following quotes
        # Strip any linebreaks
        element_text = element_text.replace("\n", " ").replace("\r", " ")
        element_text = ' '.join(element_text.split())
        # If string is empty, then return None, else encode adn return as UTF-8 and escape characters
        if element_text == "":
            return None
        else:
            return element_text.strip()

    else:
        return None


